for _lang, _d in TRANSLATIONS.items():
    base = _EXTRA_I18N.get(_lang) or _EXTRA_I18N.get('en', {})
    for _k, _v in base.items():
        if _k not in _d:
            _d[_k] = _v


def t(key: str, lang: str | None = None) -> str:
    lang = (lang or getattr(g, 'ui_lang', 'pl') or 'pl')
    if lang not in TRANSLATIONS:
        lang = 'pl'
    return TRANSLATIONS.get(lang, {}).get(key, TRANSLATIONS['pl'].get(key, key))


def install_i18n(app, get_user_by_username, get_user_preferences):
    @app.context_processor
    def inject_t():
        return {'t': t, 'ui_lang': getattr(g, 'ui_lang', 'pl')}

    @app.before_request
    def _load_ui_lang():
        try:
            me = session.get('username')
            qlang = (request.args.get('lang') or '').strip()
            lang = None
            if qlang:
                lang = qlang
            elif me:
                u = get_user_by_username('.', me) or {}
                prefs = get_user_preferences('.', int(u.get('id') or 0))
                lang = (prefs.get('lang') or '').strip()
            if lang not in LANGS:
                lang = 'pl'
            g.ui_lang = lang
        except Exception:
            g.ui_lang = 'pl'
